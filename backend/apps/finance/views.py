class SupplierInvoicePdfView(APIView):
    permission_classes = [IsAuthenticated]

    def get(self, request, invoice_id):
        invoice = get_object_or_404(
            SupplierInvoice.objects.select_related("supplier", "purchase_order"),
            id=invoice_id,
        )

        po = getattr(invoice, "purchase_order", None)
        po_items = []
        if po and hasattr(po, "items"):
            po_items = po.items.select_related("product").all()

        buffer = BytesIO()
        pdf = canvas.Canvas(buffer, pagesize=A4)

        draw_header(pdf, "Supplier Invoice", f"Invoice No: {invoice.invoice_number}")

        y = 250 * mm
        pdf.setFont("Helvetica", 10)
        pdf.drawString(20 * mm, y, f"Supplier: {getattr(invoice.supplier, 'name', '')}")
        y -= 6 * mm
        pdf.drawString(20 * mm, y, f"PO Number: {getattr(po, 'po_number', '') if po else ''}")
        y -= 6 * mm
        pdf.drawString(20 * mm, y, f"Invoice Date: {invoice.invoice_date or invoice.created_at.date()}")
        y -= 6 * mm
        pdf.drawString(20 * mm, y, f"Due Date: {invoice.due_date or '-'}")
        y -= 6 * mm
        pdf.drawString(20 * mm, y, f"Status: {invoice.status}")
        y -= 10 * mm

        pdf.setFont("Helvetica-Bold", 10)
        pdf.drawString(20 * mm, y, "Product")
        pdf.drawString(100 * mm, y, "Qty")
        pdf.drawString(125 * mm, y, "Unit Cost")
        pdf.drawString(160 * mm, y, "Line Total")
        y -= 4 * mm
        pdf.line(20 * mm, y, 190 * mm, y)
        y -= 6 * mm

        pdf.setFont("Helvetica", 10)
        for item in po_items:
            line_total = decimal_value(item.quantity) * decimal_value(item.unit_cost)
            pdf.drawString(20 * mm, y, getattr(item.product, "name", ""))
            pdf.drawString(100 * mm, y, str(item.quantity))
            pdf.drawString(125 * mm, y, f"£{money(item.unit_cost)}")
            pdf.drawString(160 * mm, y, f"£{money(line_total)}")
            y -= 7 * mm

            if y < 40 * mm:
                draw_footer(pdf)
                pdf.showPage()
                draw_header(pdf, "Supplier Invoice", f"Invoice No: {invoice.invoice_number}")
                y = 250 * mm

        y -= 4 * mm
        pdf.line(120 * mm, y, 190 * mm, y)
        y -= 8 * mm
        pdf.setFont("Helvetica-Bold", 11)
        pdf.drawString(120 * mm, y, f"Net: £{money(invoice.subtotal_amount)}")
        y -= 7 * mm
        pdf.drawString(120 * mm, y, f"Tax: £{money(invoice.tax_amount)}")
        y -= 7 * mm
        pdf.drawString(120 * mm, y, f"Total: £{money(invoice.total_amount)}")
        y -= 7 * mm
        pdf.drawString(120 * mm, y, f"Paid: £{money(invoice.amount_paid)}")
        y -= 7 * mm
        pdf.drawString(120 * mm, y, f"Balance: £{money(invoice.balance_due)}")

        draw_footer(pdf)
        pdf.save()

        buffer.seek(0)
        return FileResponse(
            buffer,
            as_attachment=True,
            filename=f"{invoice.invoice_number}.pdf",
            content_type="application/pdf",
        )


